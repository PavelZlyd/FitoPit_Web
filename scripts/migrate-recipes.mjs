const { products } = JSON.parse(fs.readFileSync(productsPath, 'utf8'));

function nutritionFor(def) {
  let kcal = 0;
  let protein = 0;
  let fat = 0;
  let carbs = 0;
  for (const ing of def.ing) {
    const p = products[ing.name];
    if (!p) {
      console.warn(`  ⚠ нет продукта в справочнике: "${ing.name}"`);
      continue;
    }
    const f = ing.grams / 100;
    kcal += p.kcalPer100g * f;
    protein += p.proteinPer100g * f;
    fat += p.fatPer100g * f;
    carbs += p.carbsPer100g * f;
  }
  const scale = 100 / def.yield;
  return {
    kcalPer100g: Math.round(kcal * scale),
    proteinPer100g: parseFloat((protein * scale).toFixed(1)),
    fatPer100g: parseFloat((fat * scale).toFixed(1)),
    carbsPer100g: parseFloat((carbs * scale).toFixed(1)),
  };
}

function buildIngredients(def) {
  return def.ing.map((ing) => {
    const out = { name: ing.name, grams: ing.grams };
    if (ing.massless || MASSLESS.has(ing.name)) out.massless = true;
    return out;
  });
}

let migrated = 0;
let missing = 0;
const missingTitles = [];

function applyRecipe(recipe) {
  if (!recipe || typeof recipe !== 'object') return recipe;
  delete recipe.url;
  const def = RECIPES[recipe.title];
  if (!def) {
    missing += 1;
    missingTitles.push(recipe.title);
    return recipe;
  }
  const n = nutritionFor(def);
  recipe.kcalPer100g = n.kcalPer100g;
  recipe.proteinPer100g = n.proteinPer100g;
  recipe.fatPer100g = n.fatPer100g;
  recipe.carbsPer100g = n.carbsPer100g;
  recipe.yieldGrams = def.yield;
  recipe.ingredients = buildIngredients(def);
  migrated += 1;
  return recipe;
}

function walk(list) {
  return (list || []).map(applyRecipe);
}

const bundle = JSON.parse(fs.readFileSync(recipesPath, 'utf8'));
